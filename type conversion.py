"""
type conversion:conversion the datatype form one format to other format.
implicit type conversion:no loss of data(lower datatypes are placing into higher datatypes)
"""
d=2345
print(d)
print(float(d))
"""
exlicit type conversion:loss of data (higher datatypes are placing into lower datatypes)
"""
d=3456.6789
print(d)
print(int(d))